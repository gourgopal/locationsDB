CREATE PROCEDURE [LOC].[Location_DELETE]
(
@ID int
)
AS
BEGIN
	BEGIN TRANSACTION;
	SAVE TRANSACTION loc_in;

	BEGIN TRY
		DELETE FROM [loc].[locations]
			WHERE [ID] = @ID
	END TRY
	BEGIN CATCH
	IF @@TRANCOUNT > 0
		BEGIN
			ROLLBACK TRANSACTION loc_in;
		END
	END CATCH
	COMMIT TRANSACTION
END;
GO

